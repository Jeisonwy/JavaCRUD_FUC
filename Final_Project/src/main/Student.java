public class Student {
    
    private int enrollment;
    private String name;
    private String firlastName;
    private String seclastName;
    private String carreerKey;
    private String genre;
    

    public int getEnrollment() {
        return enrollment;
    }

    public void setEnrollment(int enrollment) {
        this.enrollment = enrollment;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFirlastName() {
        return firlastName;
    }

    public void setFirlastName(String firlastName) {
        this.firlastName = firlastName;
    }

    public String getSeclastName() {
        return seclastName;
    }

    public void setSeclastName(String seclastName) {
        this.seclastName = seclastName;
    }

    public String getCarreerKey() {
        return carreerKey;
    }

    public void setCarreerKey(String carreerKey) {
        this.carreerKey = carreerKey;
    }

    public String getGenre() {
        return genre;
    }

    public void setGenre(String genre) {
        this.genre = genre;
    }
    

}
